    private Color randomColor(){
        switch(randomNum.nextInt(5)){ //random int [0,5)
            case 0:	return Color.YELLOW;
            case 1:	return Color.RED;
            case 2:	return Color.GREEN;	
            case 3:	return Color.BLUE;
            case 4:	return Color.MAGENTA;
        }
        return null;
    }
        
